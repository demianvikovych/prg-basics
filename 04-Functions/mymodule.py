def input_string(message):
    result_str = str(input(message))
    return result_str

def input_integer(message):
    result_int = int(input(message))
    return result_int

def input_real(message):
    result_real = abs(input(message))
    return result_real

def input_boolean(message):
    result_bool = bool(input(message))
    return result_bool
